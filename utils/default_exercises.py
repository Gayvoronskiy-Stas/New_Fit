async def add_default_exercises(db_manager, user_id: int):
    """Добавить стандартные упражнения новому пользователю"""
    added_count = 0

    for category, exercises in DEFAULT_EXERCISES.items():
        for exercise_name in exercises:
            exercise_id = await db_manager.add_exercise(
                user_id=user_id,
                name=exercise_name,
                category=category,
                exercise_type="preset"
            )
            if exercise_id:
                added_count += 1

    return added_count